#Faça um programa que receba 4 numeros inteiros , calcule e mostre a soma desses números

print ("Bem Vindo ao Soma 4: Digite quatro numeros para somar")
x = int(input ("Digite o primeiro numero"))
y = int(input ("Digite o segundo numero"))
z = int(input ("Digite o terceiro numero"))
w = int(input ("Digite o quarto numero"))

soma = x + y + z + w
print(soma)